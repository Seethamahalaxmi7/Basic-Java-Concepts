public class DataTypesExample {
    public static void main(String[] args) {
        // Defining different data types
        char letter = 'A';       // Character data type
        float floatValue = 3.14f; // Float data type (suffix 'f' is required)
        double doubleValue = 25.987654; // Double data type (higher precision)

        // Printing the variables to the console
        System.out.println("Character value: " + letter);
        System.out.println("Float value: " + floatValue);
        System.out.println("Double value: " + doubleValue);
    }
}