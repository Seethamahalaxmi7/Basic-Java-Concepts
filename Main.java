// Defining a class
class Student {
    // Method with its signature
    void displayInfo() {
        System.out.println("Hello, I am a Student!");
    }
}

public class Main {
    public static void main(String[] args) {
        // Creating an object of the class
        Student s1 = new Student();
        
        // Calling the method using the object
        s1.displayInfo();
    }
}